#!/usr/bin/env node
// Build src/photos-manifest.json from a directory of original-quality photo
// exports. Each entry has the photo's capture timestamp + GPS coordinates,
// which the web app joins onto its iCloud-fetched photo list at runtime.
//
// Usage:
//   npm run photos:manifest -- /path/to/exported/originals
//
// One-time on your Mac:
//   • In Photos.app: select all trip photos → File → Export →
//     "Export Unmodified Original" → into a folder.
//   • Run this script pointing at that folder.
//   • Commit the regenerated src/photos-manifest.json.
//
// The matching the web app does (by capture timestamp) only works when the
// EXIF DateTimeOriginal is in UTC. Modern iPhone exports always include
// OffsetTimeOriginal, so exifr converts to an absolute Date for us.

import { readdir, writeFile, stat } from "node:fs/promises";
import { join, extname, basename, dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

let exifr;
try {
  exifr = (await import("exifr")).default;
} catch {
  console.error("\nThis script needs the `exifr` package.");
  console.error("Run:  npm install --save-dev exifr\n");
  process.exit(1);
}

const IMAGE_EXTS = new Set([
  ".jpg", ".jpeg", ".heic", ".heif", ".png", ".tiff", ".tif",
]);

async function* walk(dir) {
  for (const entry of await readdir(dir, { withFileTypes: true })) {
    if (entry.name.startsWith(".")) continue;
    const path = join(dir, entry.name);
    if (entry.isDirectory()) {
      yield* walk(path);
    } else if (
      entry.isFile() &&
      IMAGE_EXTS.has(extname(entry.name).toLowerCase())
    ) {
      yield path;
    }
  }
}

async function main() {
  const inputDir = process.argv[2];
  if (!inputDir) {
    console.error("Usage: node scripts/build-photos-manifest.mjs <photos-dir>");
    process.exit(1);
  }
  const absDir = resolve(inputDir);
  try {
    const s = await stat(absDir);
    if (!s.isDirectory()) throw new Error("not a directory");
  } catch {
    console.error(`Directory not found or unreadable: ${absDir}`);
    process.exit(1);
  }

  const entries = [];
  let scanned = 0;
  let withGps = 0;
  for await (const path of walk(absDir)) {
    scanned++;
    try {
      const exif = await exifr.parse(path, {
        tiff: true,
        exif: true,
        gps: true,
      });
      if (!exif) continue;
      const date = exif.DateTimeOriginal || exif.CreateDate || null;
      if (!date) continue;
      const dateIso = new Date(date).toISOString();
      const lat = exif.latitude;
      const lng = exif.longitude;
      if (lat == null || lng == null) {
        entries.push({
          filename: basename(path),
          dateTaken: dateIso,
          width: exif.ExifImageWidth || exif.ImageWidth || null,
          height: exif.ExifImageHeight || exif.ImageHeight || null,
          lat: null,
          lng: null,
        });
      } else {
        withGps++;
        entries.push({
          filename: basename(path),
          dateTaken: dateIso,
          width: exif.ExifImageWidth || exif.ImageWidth || null,
          height: exif.ExifImageHeight || exif.ImageHeight || null,
          lat: Number(lat.toFixed(6)),
          lng: Number(lng.toFixed(6)),
        });
      }
    } catch (err) {
      console.warn(`! skipped ${basename(path)}: ${err.message}`);
    }
  }

  entries.sort((a, b) => a.dateTaken.localeCompare(b.dateTaken));

  const here = dirname(fileURLToPath(import.meta.url));
  const outPath = resolve(here, "..", "src", "photos-manifest.json");
  const payload = {
    version: 1,
    note:
      "Generated by scripts/build-photos-manifest.mjs. " +
      "Re-run after adding photos to your export folder.",
    generatedAt: new Date().toISOString(),
    entries,
  };
  await writeFile(outPath, JSON.stringify(payload, null, 2) + "\n");

  console.log(`\nScanned ${scanned} images`);
  console.log(`  with GPS:    ${withGps}`);
  console.log(`  total saved: ${entries.length}`);
  console.log(`\nManifest written: ${outPath}\n`);
}

main();
