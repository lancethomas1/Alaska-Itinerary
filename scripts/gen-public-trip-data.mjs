// Generates src/trip-data.public.js — a sanitized copy of src/trip-data.js
// with the costs array emptied and every dollar amount scrubbed from item
// strings. Run before `vite build` for the public share build so cost
// figures never enter the bundle in the first place.

import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const root = path.resolve(path.dirname(__filename), "..");
const srcPath = path.join(root, "src", "trip-data.js");
const outPath = path.join(root, "src", "trip-data.public.js");

function scrubMoney(s) {
  if (typeof s !== "string") return s;
  let out = s.replace(/\s*[—–-]\s*\$[\d,]+(?:\.\d+)?(?:\s+for\s+\d+)?/g, "");
  out = out.replace(/\$[\d,]+(?:\.\d+)?(?:\s*\([^)]*\))?\s*·?\s*/g, "");
  out = out.replace(/\s+[·—–-]\s*$/, "").replace(/\s{2,}/g, " ").trim();
  return out;
}

function sanitize(data) {
  return {
    ...data,
    costs: [],
    sections: data.sections.map((section) => ({
      ...section,
      days: section.days.map((day) => ({
        ...day,
        items: day.items
          .filter((it) => !/^Spent\s/i.test(it))
          .map(scrubMoney)
          .filter(Boolean),
      })),
    })),
  };
}

const { default: data } = await import(pathToFileURL(srcPath).href);
const cleaned = sanitize(data);

const banner = "// AUTO-GENERATED by scripts/gen-public-trip-data.mjs — do not edit by hand.\n// Source: src/trip-data.js. Regenerate via `npm run build:public`.\n\n";
const body = `const TRIP_DATA = ${JSON.stringify(cleaned, null, 2)};\n\nexport default TRIP_DATA;\n`;
await fs.writeFile(outPath, banner + body);
console.log(`wrote ${path.relative(root, outPath)} (${cleaned.sections.length} sections, costs scrubbed)`);
